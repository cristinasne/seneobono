<script>
    // Array para almacenar los fármacos seleccionados
    let farmacosSeleccionados = [];

    // Capturar el clic en los botones "Agregar al Carrito"
    const botonesAgregar = document.querySelectorAll('.boton-item');
    botonesAgregar.forEach((boton, index) => {
        boton.addEventListener('click', function () {
            // Obtener los datos del fármaco
            const item = boton.parentElement;
            const titulo = item.querySelector('.titulo-item').textContent;
            const precio = item.querySelector('.precio-item').textContent;

            // Agregar el fármaco al array
            farmacosSeleccionados.push({ titulo, precio });

            // Actualizar la lista en el carrito
            actualizarCarrito();
        });
    });

    // Función para actualizar la lista de fármacos en el carrito
    function actualizarCarrito() {
        const listaFarmacos = document.getElementById('listaFarmacos');
        listaFarmacos.innerHTML = ''; // Limpiar la lista actual

        // Recorrer los fármacos seleccionados y agregarlos al carrito
        farmacosSeleccionados.forEach((farmaco, index) => {
            const div = document.createElement('div');
            div.classList.add('carrito-item');
            div.innerHTML = `
                <span>${farmaco.titulo}</span>
                <span>${farmaco.precio}</span>
                <button class="btn-eliminar" onclick="eliminarFarmaco(${index})">Eliminar</button>
            `;
            listaFarmacos.appendChild(div);
        });
    }

    // Función para eliminar un fármaco del carrito
    function eliminarFarmaco(index) {
        farmacosSeleccionados.splice(index, 1); // Eliminar el fármaco del array
        actualizarCarrito(); // Actualizar la lista en el carrito
    }
</script>
